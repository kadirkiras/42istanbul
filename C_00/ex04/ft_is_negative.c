#include<unistd.h>

void	ft_is_negative(int c)
{
	if (c >= 0)
	{
		write(1, "P", 1);
	}
	else if (c <= 0)
	{
		write(1, "N", 1);
	}
}

int	main(void)
{
	ft_is_negative(-2);
}
